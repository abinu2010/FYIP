using UnityEngine;

public class GunRecoil : MonoBehaviour
{
    [SerializeField] private Transform gunTransform;
    [SerializeField] private float kickBack = 0.12f;
    [SerializeField] private float kickUp = 0.06f;
    [SerializeField] private float returnSpeed = 12f;

    private Vector3 defaultLocalPos;

    void Start()
    {
        // Store default gun position
        if (gunTransform != null)
        {
            defaultLocalPos = gunTransform.localPosition;
            Debug.Log("GunRecoil: Initialised at " + defaultLocalPos);
        }
        else
        {
            Debug.LogWarning("GunRecoil: Gun transform not set");
        }
    }

    void Update()
    {
        // Smoothly return gun to rest
        if (gunTransform == null)
            return;

        gunTransform.localPosition = Vector3.Lerp(
            gunTransform.localPosition,
            defaultLocalPos,
            returnSpeed * Time.deltaTime
        );
    }

    public void ApplyRecoil()
    {
        // Add simple kick to gun
        if (gunTransform == null)
            return;

        Vector3 kick = new Vector3(0f, kickUp, -kickBack);
        gunTransform.localPosition += kick;
        Debug.Log("GunRecoil: Recoil applied");
    }
}
